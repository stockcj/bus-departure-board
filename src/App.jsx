// ---------------------
// DotText component
// ---------------------
function DotText({ text }) {
  const rows = Array.from({ length: 7 }, () => []);

  text.toUpperCase().split('').forEach(char => {
    const matrix = font5x7[char] || font5x7[' ']; // fallback to space
    for (let r = 0; r < 7; r++) {
      rows[r].push(...matrix[r], 0); // 0 = spacing between letters
    }
  });

  return (
    <div style={{ display: 'inline-block' }}>
      {rows.map((row, i) => (
        <div key={i} className="dot-row">
          {row.map((dot, j) => (
            <span key={j} className={`dot ${dot ? 'on' : ''}`}></span>
          ))}
        </div>
      ))}
    </div>
  );
}

function App() {
  const [departures, setDepartures] = useState([]);

  const fetchDepartures = async () => {
    try {
      const proxy = 'https://cors-anywhere.herokuapp.com/';
      const url = proxy + 'https://www.cambridgeshirebus.info/Popup_Content/WebDisplay/WebDisplay.aspx?stopRef=0500SCOMB004&stopName=South%20Street&stopPAC=CMBDTWTP&uid=1757921719258&inIFrame=true';
      const res = await axios.get(url); 

      // Parse the HTML string into a DOM
      const parser = new DOMParser();
      const doc = parser.parseFromString(res.data, 'text/html');

      // Select table rows (replace selectors with your page's structure)
      const allRows = Array.from(doc.querySelectorAll('#gridViewRTI .gridRow')).slice(0, 5);

      const parsedDepartures = allRows.map(row => {
        const service = row.querySelector('.gridServiceItem')?.textContent.trim() || '';
        const destination = row.querySelector('.gridDestinationItem span')?.textContent.trim() || '';
        const time = row.querySelector('.gridTimeItem')?.textContent.trim() || '';
        return { service, destination, time };
      });


      setDepartures(parsedDepartures);
    } catch (err) {
      console.error('Failed to fetch departures:', err);
    }
  };

  useEffect(() => {
    fetchDepartures();
    const interval = setInterval(fetchDepartures, 60000); // refresh every minute
    return () => clearInterval(interval);
  }, []);

    return (
    <div className="board">
      <table>
        <thead>
          <tr>
            <th>Service</th>
            <th>Destination</th>
            <th>Due</th>
          </tr>
        </thead>
        <tbody>
          {departures.map((dep, i) => (
            <tr key={i}>
              <td>{dep.service}</td>
              <td>{dep.destination}</td>
              <td>{dep.time}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

export default App;
